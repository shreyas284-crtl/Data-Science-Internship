CREATE TABLE customers (
customer_id INT PRIMARY KEY,
customer_name VARCHAR(100),
segment VARCHAR(50),
country VARCHAR(50),
city VARCHAR(100),
state VARCHAR(50),
postal_code INT,
region VARCHAR(50)
);

CREATE TABLE orders (
order_id INT PRIMARY KEY,
order_date DATE,
ship_date DATE,
ship_mode VARCHAR(50),
customer_id INT ,
product_id INT,
category VARCHAR(50),
sub_category VARCHAR(50),
product_name VARCHAR(100),
sales FLOAT,
quantity INT,
discount FLOAT,
profit FLOAT
);
